"""
Degree-preserving edge-rewiring null (referee test).

For each non-degenerate held-out cluster (LOCO, model seed 42, 24h horizon), trains
the GAT on the genuine directed lead-lag graphs and on graphs whose edge destinations
are permuted (preserving edge count, source out-degrees, and the edge-attribute
multiset) via the ``SCGNN_REWIRE_SEED`` hook in ``scgnn.data.dataset``. If the GAT's
gain survives rewiring, it came merely from having edges; if it collapses to the base
rate, the genuine topology is what the GAT exploits.

Writes results/eval/edge_rewiring_null.json. Deterministic.

Usage:
    python eval/run_edge_rewiring_null.py                # rewire seeds 1..5
    python eval/run_edge_rewiring_null.py --seeds 1 2 3   # reproduce the 3-seed result
"""
from __future__ import annotations

import argparse
import json
import os
import sys
from pathlib import Path

import numpy as np

_ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(_ROOT / "src"))
sys.path.insert(0, str(_ROOT / "eval"))

from run_benchmark import cluster_of  # noqa: E402
from scgnn.data.dataset import list_episodes, load_feature_names, tabular_from_episodes  # noqa: E402
from scgnn.eval.metrics import full_report  # noqa: E402
from scgnn.models.classical import make_xgboost  # noqa: E402
from scgnn.models.gnn_trainer import GNNContagionTrainer  # noqa: E402
from scgnn.utils.seeds import set_all_seeds  # noqa: E402

H = 1440
MODEL_SEED = 42
FOLDS = ["SVB_2023", "FTX_2022", "Terra_2022"]


def _gat_pr(train, test, feat, yte) -> float:
    set_all_seeds(MODEL_SEED)
    tr = GNNContagionTrainer(kind="gat", horizon=H, seed=MODEL_SEED, epochs=80, patience=10)
    tr.fit(train, None)
    return float(full_report(yte, tr.predict_episodes(test))["pr_auc"])


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--seeds", type=int, nargs="+", default=[1, 2, 3, 4, 5],
                    help="Rewire seeds (env SCGNN_REWIRE_SEED). Default 1..5.")
    args = ap.parse_args()

    feat = load_feature_names()
    eps = list_episodes()

    out = {
        "description": (
            "Degree-preserving edge-rewiring null test. GAT trained on real directed "
            "lead-lag graphs vs graphs whose edge destinations are permuted (preserving "
            "edge count, source out-degrees, and edge attributes). Model seed 42; LOCO "
            "all_7; 24h horizon. Tests whether GAT's gain is from the genuine topology "
            "or merely from having edges. Generated by eval/run_edge_rewiring_null.py."
        ),
        "rewire_seeds": list(args.seeds),
        "folds": {},
    }

    for fold in FOLDS:
        test = [e for e in eps if cluster_of(e) == fold]
        train = [e for e in eps if cluster_of(e) != fold]
        Xtr, ytr, _ = tabular_from_episodes(train, H, feat)
        Xte, yte, _ = tabular_from_episodes(test, H, feat)
        base = float(yte.mean())

        spw = (len(ytr) - ytr.sum()) / max(ytr.sum(), 1)
        xgb = make_xgboost(scale_pos_weight=spw)
        xgb.fit(Xtr, ytr, verbose=False)
        xgb_pr = float(full_report(yte, xgb.predict_proba(Xte)[:, 1])["pr_auc"])

        os.environ.pop("SCGNN_REWIRE_SEED", None)
        gat_real = _gat_pr(train, test, feat, yte)

        rewired = []
        for k in args.seeds:
            os.environ["SCGNN_REWIRE_SEED"] = str(k)
            rewired.append(round(_gat_pr(train, test, feat, yte), 4))
        os.environ.pop("SCGNN_REWIRE_SEED", None)

        out["folds"][fold] = {
            "base_rate": round(base, 4),
            "xgboost": round(xgb_pr, 4),
            "gat_real": round(gat_real, 4),
            "gat_rewired": rewired,
            "gat_rewired_mean": round(float(np.mean(rewired)), 4),
            "gat_rewired_std": round(float(np.std(rewired)), 4),
        }
        print(f"{fold}: base={base:.4f} xgb={xgb_pr:.4f} gat_real={gat_real:.4f} "
              f"rewired={rewired} mean={out['folds'][fold]['gat_rewired_mean']:.4f} "
              f"std={out['folds'][fold]['gat_rewired_std']:.4f}")

    dest = _ROOT / "results" / "eval" / "edge_rewiring_null.json"
    dest.write_text(json.dumps(out, indent=2))
    print(f"\nWrote {dest}")


if __name__ == "__main__":
    main()
